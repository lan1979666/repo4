package com.itheima_02;

public class MathDemo {
    public static void main(String[] args){
        System.out.println(Math.abs(88));
        System.out.println(Math.abs(-88));
        System.out.println("--------");
        System.out.println(Math.ceil(13.2));
        System.out.println(Math.ceil(13.6));
        System.out.println("--------");
        System.out.println(Math.floor(13.25));
        System.out.println(Math.floor(13.65));
        System.out.println("--------");
        System.out.println(Math.max(80,90));
        System.out.println(Math.min(15,60));
        System.out.println(Math.pow(2.0,3.0));
        //System.exit(0);
        System.out.println("--------");
        System.out.println((int)(Math.random()*100)+1);
        System.out.println(System.currentTimeMillis());


    }
}
